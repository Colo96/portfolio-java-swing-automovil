    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        if (table.getRowCount() > 0) {
            if (table.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(table.getValueAt(table.getSelectedRow(), 0)));
                control.borrarAuto(id);
                mostrarMensaje("Auto borrado correctamente!", "Info", "Borrado exitoso!");
                cargarTabla();
            } else {
                mostrarMensaje("No hay seleccionada ninguna fila", "Error", "Fila no encontrada!");
            }
        } else {
            mostrarMensaje("No se encontro ningun automovil", "Error", "Tabla vacia!");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

