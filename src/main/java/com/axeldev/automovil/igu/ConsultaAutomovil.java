    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        if (table.getRowCount() > 0) {
            if (table.getSelectedRow() != -1) {
                int id = Integer.parseInt(String.valueOf(table.getValueAt(table.getSelectedRow(), 0)));
                EditarAutomovil editar = new EditarAutomovil(id);
                editar.setVisible(true);
                editar.setLocationRelativeTo(null);
                this.dispose();
            } else {
                mostrarMensaje("No hay seleccionada ninguna fila", "Error", "Fila no encontrada!");
            }
        } else {
            mostrarMensaje("No se encontro ningun automovil", "Error", "Tabla vacia!");
        }
    }//GEN-LAST:event_btnEditarActionPerformed


